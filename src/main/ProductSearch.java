    boolean ReportLowStockVal = true;
    private void ReportLowStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReportLowStockActionPerformed
        // TODO add your handling code here:
        // still learn by analogy
        TableRowSorter<DefaultTableModel> sorter;
        sorter = new TableRowSorter<>((DefaultTableModel) ProductTable.getModel());
        ProductTable.setRowSorter(sorter);
        
        if(ReportLowStockVal){
            sorter.setRowFilter(new RowFilter<DefaultTableModel, Integer>() {
            @Override
                public boolean include(RowFilter.Entry<? extends DefaultTableModel, ? extends Integer> entry) {
                    String stockStr = entry.getStringValue(5); // 第 5 列 = stock
                    try {
                        int stock = Integer.parseInt(stockStr);
                        ReportLowStockVal = false;
                        return stock <= 50;   // 显示库存小于 50 的
                    } catch (NumberFormatException e) {
                        return false; // 非数字就不显示
                    }
                }
            });
        }else{
            sorter.setRowFilter(null);
            ReportLowStockVal = true;
            return;
        }
    }//GEN-LAST:event_ReportLowStockActionPerformed

