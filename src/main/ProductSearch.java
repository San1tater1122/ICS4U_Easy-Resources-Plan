    private void byNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_byNameActionPerformed
        // TODO add your handling code here:
        // GPT's method
        TableRowSorter<DefaultTableModel> sorter;
        sorter = new TableRowSorter<>((DefaultTableModel) ProductTable.getModel());
        ProductTable.setRowSorter(sorter);
        
        String keyword = searchTextField.getText().trim();

        // 如果输入为空 → 显示全部
        if (keyword.isEmpty()) {
            sorter.setRowFilter(null);
            return;
        }

        // 自定义 RowFilter → 完全匹配 Name（列 1）
        sorter.setRowFilter(new RowFilter<DefaultTableModel, Integer>() {
        @Override
            public boolean include(RowFilter.Entry<? extends DefaultTableModel, ? extends Integer> entry) {
                String name = entry.getStringValue(1); // 列 1 = ProductName
                return name.equals(keyword);
            }
        });
    }//GEN-LAST:event_byNameActionPerformed

